#include "3-calc.h"

/**
 * op_add - Function to add two integers
 * @a: First integer
 * @b: Second integer
 * Return: The sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function to subtract between two integers
 * @a: First integer
 * @b: Second integer
 * Return: The result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function to multiply two integers
 * @a: First integer
 * @b: Second integer
 * Return: The result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function to divide one integer by another
 * @a: First integer
 * @b: Second integer
 * Return: The result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function to get the remainder from two numbers
 * @a: First integer
 * @b: Second integer
 * Return: The result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
